    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        //1. Consultamos mediante la función getText() el valor que el usuario
        //ingreso en el campo de usuario y de contraseña
        String username = txtUsuario.getText();
        String password = txtPassword.getText();
        
        System.out.println("username: " + username + ", password: "+ password);
        //2. Validar si alguno de los dos campos esta vacío
        if(username.isEmpty() || password.isEmpty()){
            JOptionPane.showMessageDialog(this, "Hay campos sin diligenciar.", "Login", JOptionPane.WARNING_MESSAGE);
        }else{
            //3. Validar el escenario en que la información corresponde a los datos de inicio de sesión
            if(username.equals("MisionTIC") && password.equals("Ciclo2")){
                JOptionPane.showMessageDialog(this, "Bienvenido al sistema");
                //4. Crear una instancia de la vista UserMenu
                UserMenu userMenu = new UserMenu();
                //5. Hacer visible la vista mediante el método setVisible()
                userMenu.setVisible(true);
                //6. Eliminar la vista de Login
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(this, "Error en los datos de inicio de sesión", "Login", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

